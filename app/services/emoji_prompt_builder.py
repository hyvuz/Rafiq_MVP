def build_emoji_prompt(simplified_text: str, step_texts: list[str], emoji_candidates: list[str]) -> str:
    candidates_text = ", ".join(emoji_candidates) if emoji_candidates else "لا توجد رموز مناسبة"
    steps_text = "\n".join([f"- {step}" for step in step_texts]) if step_texts else f"- {simplified_text}"

    prompt = f"""
أنت مساعد ذكي يضيف دعماً بصرياً بالنصوص العربية المبسطة للصم وضعاف السمع.

الهدف:
اختيار emojis تساعد المستخدم على فهم النص بسرعة أكبر، مع الالتزام بسهولة القراءة ومبادئ WCAG 2.2.

قواعد مهمة:
- النص هو الأساس، والـ emoji دعم بصري فقط.
- استخدم من 2 إلى 4 emojis لكل خطوة إذا كانت القائمة المسموح بها تحتوي على عدد كافٍ.
- إذا كانت القائمة المسموح بها تحتوي على أقل من 2، استخدم المتاح فقط.
- لا تستخدم أكثر من 4 emojis أبدًا.
- لا تستخدم أي emoji للزينة.
- لا تستخدم emoji إذا لم يضف وضوحًا حقيقيًا.
- استخدم فقط emojis الموجودة في القائمة المسموح بها.
- لا تكرر نفس الـ emoji بدون حاجة.
- اختر emojis تمثل المعنى بوضوح، مثل:
  - الحركة
  - الاتجاه
  - المكان
  - التحذير
  - الحالة
- أعطِ الأولوية لاختيار مجموعة emojis متنوعة تغطي المعنى بدل تكرار نفس النوع.
- إذا احتوت الجملة على "لا" أو "ممنوع"، أعطِ أولوية لرموز المنع أو الرفض مثل ⛔ أو 🙅 أو 🚫 إذا كانت موجودة في القائمة.
- إذا كان هناك تحذير أو ازدحام، أعطِ أولوية لرموز التحذير والناس.
- تجنب الرموز الغامضة أو المجردة إذا لم تكن واضحة مباشرة.
- لا تختر emoji إذا كان يضيف معنى غامضًا أو غير مباشر.
- إذا كانت الخطوة تتعلق بالجمرات وكان 🪨 أو 🎯 متاحًا، فاختر الرمز أو الرمزين الأنسب.
- إذا كانت الخطوة تتعلق بالدخول أو البوابة، يمكن استخدام 🚪 إذا كان مفيدًا.
- اختر الرموز الأكثر وضوحًا في السياق، وليس الأكثر عددًا.

الـ emojis المسموح بها:
{candidates_text}

النص المبسط:
{simplified_text}

الخطوات:
{steps_text}

أخرج JSON فقط بهذا الشكل:
{{
  "emoji_support": [
    {{
      "step_text": "",
      "emojis": ["", ""]
    }}
  ]
}}
"""
    return prompt